# program to input two whole numbers,
# add them together and print the result to the screen. 
def main():
    n1= input("Enter your first number ")
    n2= input("Enter your second number ")
    n3= input("Enter your third number ")
    n4= input("Enter your forth number ")
    sumOfNumbers = int(n1 + n2 + n3 + n4)
    print("{0} + {1} + {2} + {3} = {4}".format(n1,n2,n3,n4,sumOfNumbers))
main()    
  
